public class Conflicts {

	String day;
	
	String FirstCourseName;
	String SecCourseName;
	int FirstCourseRunNo;
	int SecCourseRunNo;
	
	int conflictReason;
	
	String Name;

	/**
	 * @return the day
	 */
	public String getDay() {
		return day;
	}

	/**
	 * @param day the day to set
	 */
	public void setDay(String day) {
		this.day = day;
	}

	/**
	 * @return the firstCourseName
	 */
	public String getFirstCourseName() {
		return FirstCourseName;
	}

	/**
	 * @param firstCourseName the firstCourseName to set
	 */
	public void setFirstCourseName(String firstCourseName) {
		FirstCourseName = firstCourseName;
	}

	/**
	 * @return the secCourseName
	 */
	public String getSecCourseName() {
		return SecCourseName;
	}

	/**
	 * @param secCourseName the secCourseName to set
	 */
	public void setSecCourseName(String secCourseName) {
		SecCourseName = secCourseName;
	}

	/**
	 * @return the firstCourseRunNo
	 */
	public int getFirstCourseRunNo() {
		return FirstCourseRunNo;
	}

	/**
	 * @param firstCourseRunNo the firstCourseRunNo to set
	 */
	public void setFirstCourseRunNo(int firstCourseRunNo) {
		FirstCourseRunNo = firstCourseRunNo;
	}

	/**
	 * @return the secCourseRunNo
	 */
	public int getSecCourseRunNo() {
		return SecCourseRunNo;
	}

	/**
	 * @param secCourseRunNo the secCourseRunNo to set
	 */
	public void setSecCourseRunNo(int secCourseRunNo) {
		SecCourseRunNo = secCourseRunNo;
	}

	/**
	 * @return the conflictReason
	 */
	public int getConflictReason() {
		return conflictReason;
	}

	/**
	 * @param conflictReason the conflictReason to set
	 */
	public void setConflictReason(int conflictReason) {
		this.conflictReason = conflictReason;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return Name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Name = name;
	}
	

	
}
